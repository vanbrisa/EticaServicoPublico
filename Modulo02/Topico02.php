                        <p>É importante observar essas exigências e princípios, de modo que a formação de uma consciência de uma ética pública suponha um conjunto de conhecimentos e ações que cheguem a se transformar em um hábito para o funcionário e gestor público. Assim, a atividade pública estará presidida por um conjunto de valores humanos, que estão inseparavelmente unidos à ideia de serviço, facilitando e tornando possível uma noção de sensibilidade ante o público, o coletivo, de forma que a dimensão individual da ética não pode estar desligada de sua dimensão social. Por essa razão, faz-se possível falar em ética no serviço público ou na administração pública.</p>
                        <p>O entendimento da administração pública nas sociedades democráticas é de que esta presta serviço à sociedade e aí está albergada a sua verdadeira razão de ser, isto é, em atender aos interesses e direitos dos cidadãos e dos diferentes grupos que a compõem, perseguindo o equilíbrio entre esses agentes e, por certo, compatibilizando os conhecidos princípios da administração pública: legalidade, impessoalidade, moralidade, publicidade, eficiência. Ora, por sociedade democrática entende-se aquela que está fundamentada no Estado democrático de direito, isto é, aquele que está submetido às leis e ao direito. Seu surgimento tem o ideal de equilibrar as forças entre o poder e a liberdade humana. Também se integram a esse princípio, a transparência, a fiscalização e a prestação de contas. Nesse nosso modelo de democracia indireta ou representativa, o poder e as decisões são baseados em mecanismos de transferência e delegação, dentre os quais o expoente máximo é o voto. Em um governo democrático e representativo, deve ser assegurado, pois, aos cidadãos o direito à liberdade de associação, liberdade de expressão, liberdade de voto, eleições livres e imparciais e instituições que garantam o cumprimento desses requisitos.</p>
                        <p>Nesse sentido, de forma ideal, os bens comuns da sociedade deveriam ser administrados por pessoas com sólida formação ética individual e com uma ideia definida de forte compromisso social. Por exemplo, já na obra A república, de Platão, encontramos antecedentes da ideia do serviço público, como também as características e qualidades que deveriam cumprir aqueles que se dedicam a esse ofício. A partir de então, adverte-se sobre essa dupla dimensão do indivíduo: por uma parte, enquanto ser humano e, por outra, enquanto cidadão, que desempenha um papel específico dentro do corpus social e de sua organização política.</p>
                        <p>A reflexão ética sobre a dupla dimensão humana tem permanecido ao longo da história do pensamento ocidental. As contribuições de Santo Tomás de Aquino e Santo Agostinho, por exemplo, são um modelo disso. Ademais, para o filósofo alemão Immanuel Kant, como vimos, a lei moral impõe a todo ser racional exigências categóricas sobre o que se deve fazer ou o que se deve evitar, assim como exigências que obrigam o indivíduo a agir de certa maneira, independentemente de suas próprias inclinações, propósitos e interesses.</p>
                        <p>A despeito de se dizer que vivemos em uma época na qual se afirma a perda dos valores como um sinal do nosso tempo – chamado por alguns de “pós-modernidade” –, todavia, também é possível asseverar que os valores mesmos não se perdem. Ou seja, o que se pode perder seria o interesse por alcançá-los e promovê-los. É nesse contexto que reaparece a urgência ética como busca do que se pode chamar de “solidariedade humana fundamental”, e com a necessidade do estabelecimento de um consenso racional mínimo que nos possibilite uma nova postura social diante dos dilemas enfrentados na sociedade.</p>
                        <p>Diante das exigências da sociedade para que a administração pública funcione de forma ligada aos princípios éticos, governos em diversas partes do mundo têm estruturado marcos normativos, envolvendo diferentes setores da sociedade e estabelecem programas que visam à obtenção dos fins desejados. Com isso, poderíamos dizer que uma administração pública, regida por princípios éticos, estaria configurada, desenvolvida e unida por uma estrutura de princípios, ideias e práticas que conduzem e iluminam todos os seus processos e ações.</p>



